package ddr.example.com.svgmapclient.entity.other;

public class Sensor {
    private String key;
    private String dydistance;
    private String staticdistance;



    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getDydistance() {
        return dydistance;
    }

    public void setDydistance(String dydistance) {
        this.dydistance = dydistance;
    }

    public String getStaticdistance() {
        return staticdistance;
    }

    public void setStaticdistance(String staticdistance) {
        this.staticdistance = staticdistance;
    }
}
