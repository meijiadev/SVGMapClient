package ddr.example.com.svgmapclient.entity.other;

public class HardwareStatItem {
    private String data;
    private int type;
    private int state;

    public String getData() {
        return data;
    }

    public int getType() {
        return type;
    }

    public int getState() {
        return state;
    }

    public void setData(String data) {
        this.data = data;
    }

    public void setType(int type) {
        this.type = type;
    }

    public void setState(int state) {
        this.state = state;
    }
}
